package com.restaurantandcafeapplication.domainclass;

public class BillThings {
	
	int idFoodDrink;
	String nameFoodDrink;
	double price;
	
	
	public BillThings() {
		
		
	}


	public int getIdFoodDrink() {
		return idFoodDrink;
	}


	public void setIdFoodDrink(int idFoodDrink) {
		this.idFoodDrink = idFoodDrink;
	}


	public String getNameFoodDrink() {
		return nameFoodDrink;
	}


	public void setNameFoodDrink(String nameFoodDrink) {
		this.nameFoodDrink = nameFoodDrink;
	}


	public double getPrice() {
		return price;
	}


	public void setPrice(double price) {
		this.price = price;
	}
	
	
	
	

}
